package com.mmihaylov.mobile_import_car_tool;

public class MobileCarImportException extends Exception {

    public MobileCarImportException() {
        super();
    }

    public MobileCarImportException(String msg) {
        super(msg);
    }

    public MobileCarImportException(Throwable throwable) {
        super(throwable);
    }

    public MobileCarImportException(String msg, Throwable throwable) {
        super(msg, throwable);
    }

}
